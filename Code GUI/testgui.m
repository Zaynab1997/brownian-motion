% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
c=get(handles.edit3,'String');
aa=str2double(c);%conversion
c=get(handles.edit1,'String');
r0=str2double(c);%conversion
c=get(handles.edit2,'String');
f0=str2double(c);%conversion
c=get(handles.edit4,'String');
N=str2double(c);%conversion
c=get(handles.edit5,'String');
tmax=str2double(c);%conversion
display([r0,f0,tmax,N,aa]);
[yy,t]=wsyssecRK4(@F,r0,f0,0,tmax,N,aa);
vv=get(handles.radiobutton1,'value')%
if (vv==1) 
    
axes(handles.axes1)%pour tracer cette fonction sur le premier axe
plot(yy(:,1),yy(:,2),'*');grid;
axes(handles.axes2)
plot(t,yy(:,1));grid;
axes(handles.axes3)
plot(t,yy(:,2));grid;

else
figure(2);
subplot(311);
plot(yy(:,1),yy(:,2),'*');grid;
subplot(312);
plot(t,yy(:,1));grid;
subplot(313);
plot(t,yy(:,2));grid;
end ;    

